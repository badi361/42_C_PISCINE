int	ft_str_is_alpha(char *str)
{
	int	k;

	k = 0;
	if (str[k] == '\0')
	{
		return (1);
	}
	while (str[k] != '\0')
	{
		if ((str[k] >= 'A' && str[k] <= 'Z')
			|| (str[k] >= 'a' && str[k] <= 'z'))
				k++;
		else
			return (0);
	}
	return (1);
}
